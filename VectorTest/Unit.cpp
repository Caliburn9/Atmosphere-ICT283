#include "unit.h"

#include <string>

//---------------------------------------------------------------------------------------

Unit::Unit()
{
    m_id = "";
    m_name = "";
    m_credits = 0;
}

//---------------------------------------------------------------------------------------

Unit::Unit(const std::string & id, const std::string & name, int cred)
{
    m_id = id;
    m_name = name;
    m_credits = cred;
}

//---------------------------------------------------------------------------------------

int Unit::GetCredits() const
{
    return m_credits;
}

//---------------------------------------------------------------------------------------

void Unit::SetCredits(int cred)
{
    m_credits = cred;
}

//---------------------------------------------------------------------------------------

const std::string & Unit::GetId() const
{
    return m_id;
}

//---------------------------------------------------------------------------------------

void Unit::SetId(const std::string & id)
{
    m_id = id;
}

//---------------------------------------------------------------------------------------

const std::string & Unit::GetName() const
{
    return m_name;
}

//---------------------------------------------------------------------------------------

void Unit::SetName(const std::string & name)
{
    m_name = name;
}

//---------------------------------------------------------------------------------------
