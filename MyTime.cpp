#include "mytime.h"

//---------------------------------------------------------------------------------------

MyTime::MyTime()
{
    m_hour = 0;
    m_minute = 0;
}

//---------------------------------------------------------------------------------------

MyTime::MyTime(int hour, int minute)
{
    m_hour = hour;
    m_minute = minute;
}

//---------------------------------------------------------------------------------------

void MyTime::SetHour(int hour)
{
    m_hour = hour;
}

//---------------------------------------------------------------------------------------

int MyTime::GetHour() const
{
    return m_hour;
}

//---------------------------------------------------------------------------------------

void MyTime::SetMinute(int minute)
{
    m_minute = minute;
}

//---------------------------------------------------------------------------------------

int MyTime::GetMinute() const
{
    return m_minute;
}

//---------------------------------------------------------------------------------------
