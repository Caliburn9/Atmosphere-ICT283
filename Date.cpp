#include "date.h"

//---------------------------------------------------------------------------------------

Date::Date()
{
    m_day = 1;
    m_month = 1;
    m_year = 1;
}

//---------------------------------------------------------------------------------------

Date::Date(int day, int month, int year)
{
    m_day = day;
    m_month = month;
    m_year = year;
}

//---------------------------------------------------------------------------------------

int Date::GetDay() const
{
    return m_day;
}

//---------------------------------------------------------------------------------------

void Date::SetDay(int day)
{
    m_day = day;
}

//---------------------------------------------------------------------------------------

int Date::GetMonth() const
{
    return m_month;
}

//---------------------------------------------------------------------------------------

void Date::SetMonth(int month)
{
    m_month = month;
}

//---------------------------------------------------------------------------------------

int Date::GetYear() const
{
    return m_year;
}

//---------------------------------------------------------------------------------------

void Date::SetYear(int year)
{
    m_year = year;
}

//---------------------------------------------------------------------------------------
